async def _clear_seed_data(session: AsyncSession) -> None:
    """清空所有种子案例（按 is_seed 标记或标题匹配）及其向量切片、卡片、叙事。"""
    seed_titles = [case["title"] for case in _SEED_CASES]
    result = await session.execute(
        select(CaseNarrative.narrative_id).where(
            (CaseNarrative.is_seed == True) | (CaseNarrative.title.in_(seed_titles))  # noqa: E712
        )
    )
    narrative_ids = [row[0] for row in result.fetchall()]

    if narrative_ids:
        result = await session.execute(select(CaseCard.card_id).where(CaseCard.narrative_id.in_(narrative_ids)))
        card_ids = [row[0] for row in result.fetchall()]

        if card_ids:
            chunk_result = await session.execute(select(CaseChunk.id).where(CaseChunk.card_id.in_(card_ids)))
            chunk_count = len(chunk_result.fetchall())

            await session.execute(CaseChunk.__table__.delete().where(CaseChunk.card_id.in_(card_ids)))
            await session.execute(CaseCard.__table__.delete().where(CaseCard.card_id.in_(card_ids)))
            print(f"[CLEAN] 删除 {chunk_count} 条向量切片 + {len(card_ids)} 张 L2 卡片")

        narrative_result = await session.execute(
            CaseNarrative.__table__.delete().where(CaseNarrative.narrative_id.in_(narrative_ids))
        )
        print(f"[CLEAN] 删除 {narrative_result.rowcount} 条 L1 叙事")

    await session.commit()


def _map_severity(raw: str) -> str:
    """将提取输出的严重程度短形式映射为数据库枚举值。"""
    return _SEVERITY_MAP.get(raw, raw)


def _map_evidence(raw: str) -> str:
    """将提取输出的循证等级编码映射为数据库枚举值。"""
    return _EVIDENCE_MAP.get(raw, raw)


async def _import_cases(session: AsyncSession, author_id: UUID) -> list[CaseCard]:
    """导入案例数据（L1 叙事 + L2 卡片），返回创建的卡片列表。"""
    created_cards: list[CaseCard] = []

    for case_data in _SEED_CASES:
        # 幂等：按标题 + is_seed 检查是否已存在
        existing_result = await session.execute(
            select(CaseNarrative).where(
                CaseNarrative.title == case_data["title"],
                CaseNarrative.is_seed == True,  # noqa: E712
            )
        )
        if existing_result.scalars().first() is not None:
            print(f"[SKIP] 案例已存在: {case_data['title']}")
            continue

        narrative_id = uuid.uuid4()
        card_ids: list[str] = []
        card_objects: list[CaseCard] = []

        for _ in case_data["cards"]:
            card_id = uuid.uuid4()
            card_ids.append(str(card_id))

        narrative = CaseNarrative(
            narrative_id=narrative_id,
            title=case_data["title"],
            narrative=case_data["narrative"],
            source_type=SEED_SOURCE_TYPE,
            author_id=author_id,
            status="approved",
            extraction_status="extracted",
            derived_card_ids=card_ids,
            is_seed=True,
        )
        session.add(narrative)
        await session.flush()

        for idx, card_data in enumerate(case_data["cards"]):
            # 复用 narrative.derived_card_ids 中预生成的 ID，保持两者一致
            card_id = uuid.UUID(card_ids[idx])
            age_range = card_data.get("age_range", [0, 0])
            card = CaseCard(
                card_id=card_id,
                narrative_id=narrative_id,
                title=card_data["title"],
                scenario=card_data["scenario"],
                behavior_type=card_data["behavior_type"],
                age_range_min=int(age_range[0]),
                age_range_max=int(age_range[1]),
                severity=_map_severity(card_data["severity_level"]),
                scene=card_data["setting"],
                ebp_labels=card_data["ebp_labels"],
                family_category=card_data["family_category"],
                immediate_action=card_data["immediate_action"],
                comforting_phrase=card_data["comforting_phrase"],
                observation_metrics=card_data["observation_metrics"],
                medical_criteria=card_data["medical_criteria"],
                evidence_level=_map_evidence(card_data["evidence_level"]),
                caution_notes=card_data["caution_notes"],
                contraindications=card_data["contraindications"],
                is_template=bool(card_data.get("is_template", False)),
                excluded_population=card_data.get("excluded_population"),
                attachment_refs=card_data.get("attachment_refs") or [],
                review_status="approved",
                index_status="pending",
            )
            session.add(card)
            card_objects.append(card)

        await session.flush()
        created_cards.extend(card_objects)
        print(f"[OK] 案例已导入: {case_data['title']} ({len(card_objects)} 张卡片)")

    await session.commit()
    return created_cards


async def _index_cards(session: AsyncSession, cards: list[CaseCard]) -> None:
    """为指定卡片生成嵌入向量并写入 pgvector。"""
    for card in cards:
        if card.index_status == "indexed":
            print(f"[SKIP] 已索引: {card.title}")
            continue

        # 构造 pipeline 期望的数据字典
        case_data = {
            "case_id": str(card.card_id),
            "title": card.title,
            "immediate_action": card.immediate_action,
            "comforting_phrase": card.comforting_phrase,
            "observation_metrics": card.observation_metrics,
            "medical_criteria": card.medical_criteria,
            "behavior_type": card.behavior_type,
            "severity": card.severity,
            "age_range_min": card.age_range_min,
            "age_range_max": card.age_range_max,
            "evidence_level": card.evidence_level,
            "scene": card.scene,
            "source_type": SEED_SOURCE_TYPE,
        }

        try:
            chunk_text, metadata = build_chunk_text(case_data)
        except Exception as exc:
            print(f"[ERROR] 文本组装失败 [{card.title}]: {exc}")
            card.index_status = "indexing_failed"
            await session.commit()
            continue

        try:
            embedding = await encode_text(chunk_text, text_type="document")
        except Exception as exc:
            print(f"[ERROR] 嵌入编码失败 [{card.title}]: {exc}")
            card.index_status = "indexing_failed"
            await session.commit()
            continue

        try:
            await write_index_to_pgvector(
                card_id=str(card.card_id),
                chunk_text=chunk_text,
                embedding=list(embedding),
                metadata=metadata,
                db_session=session,
            )
            card.index_status = "indexed"
            card.indexed_at = datetime.now(timezone.utc)
            await session.commit()
            print(f"[OK] 向量索引完成: {card.title}")
        except Exception as exc:
            print(f"[ERROR] 索引写入失败 [{card.title}]: {exc}")
            card.index_status = "indexing_failed"
            await session.commit()


async def _repair_derived_card_ids(session: AsyncSession) -> int:
    """修正所有叙事与其真实卡片 ID 不一致的 derived_card_ids。

    按 CaseCard.created_at 升序重建 derived_card_ids，仅当现有值与期望值
    不一致时才更新，避免覆盖其他字段或已经正确的数据。
    """
    from sqlalchemy import select

    result = await session.execute(select(CaseNarrative))
    narratives = list(result.scalars().all())

    repaired = 0
    skipped = 0

    for narrative in narratives:
        card_result = await session.execute(
            select(CaseCard).where(CaseCard.narrative_id == narrative.narrative_id).order_by(CaseCard.created_at.asc())
        )
        cards = list(card_result.scalars().all())
        expected = [str(c.card_id) for c in cards]
        current = narrative.derived_card_ids or []

        if current != expected:
            narrative.derived_card_ids = expected if expected else []
            repaired += 1
            action = "清空" if not expected else f"重排 {len(expected)} 张卡片"
            print(f"[REPAIR] {narrative.title}: {action}")
        else:
            skipped += 1

    if repaired:
        await session.commit()

    print(f"[INFO] derived_card_ids 修复: {repaired} 条修正, {skipped} 条无需处理")
    return repaired


async def _reset_database(session: AsyncSession, yes: bool = False) -> bool:
    """将数据库恢复到种子数据刚注入的状态。

    删除全部案例（L1 叙事、L2 卡片、向量切片）和咨询历史记录，
    但保留用户账号与默认患者档案。执行前会要求二次确认。

    Args:
        session: 活动数据库异步会话。
        yes: 是否跳过交互式确认。

    Returns:
        True 表示已执行重置，False 表示用户取消。
    """
    # 先统计待删除数据量，用于确认提示
    narrative_count = await session.scalar(select(func.count()).select_from(CaseNarrative))
    card_count = await session.scalar(select(func.count()).select_from(CaseCard))
    chunk_count = await session.scalar(select(func.count()).select_from(CaseChunk))
    consult_count = await session.scalar(select(func.count()).select_from(ConsultationHistory))

    print("[WARN] 即将重置数据库到种子数据初始状态：")
    print(f"       - 删除 {narrative_count or 0} 条 L1 叙事")
    print(f"       - 删除 {card_count or 0} 张 L2 卡片")
    print(f"       - 删除 {chunk_count or 0} 条向量切片")
    print(f"       - 删除 {consult_count or 0} 条咨询历史")
    print("       - 保留用户账号与默认患者档案")

    if not yes:
        answer = input("确认重置吗？输入 'yes' 继续: ")
        if answer.strip().lower() != "yes":
            print("[CANCEL] 已取消重置")
            return False

    # 由于 FK 设置了 ondelete=CASCADE，删除叙事会自动级联删除卡片与切片
    await session.execute(CaseNarrative.__table__.delete())
    await session.execute(ConsultationHistory.__table__.delete())
    await session.commit()

    print("[OK] 数据库已清空，开始重新注入种子数据...")
    return True



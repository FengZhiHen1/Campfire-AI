async def main() -> int:
    parser = argparse.ArgumentParser(description="Campfire-AI 统一种子注入脚本")
    parser.add_argument(
        "--clean",
        action="store_true",
        help="清空种子数据后重新注入",
    )
    parser.add_argument(
        "--users",
        action="store_true",
        help="只注入评委用户",
    )
    parser.add_argument(
        "--profile",
        action="store_true",
        help="只注入个人档案",
    )
    parser.add_argument(
        "--cases",
        action="store_true",
        help="只注入案例库",
    )
    parser.add_argument(
        "--events",
        action="store_true",
        help="只注入个人档案事件记录",
    )
    parser.add_argument(
        "--repair-derived-ids",
        action="store_true",
        help="仅修正 case_narratives.derived_card_ids 与实际卡片 ID 不一致的数据",
    )
    parser.add_argument(
        "--reset",
        action="store_true",
        help="危险：清空全部案例与咨询历史后重新注入种子数据",
    )
    parser.add_argument(
        "--yes",
        action="store_true",
        help="配合 --reset 使用，跳过二次确认",
    )
    args = parser.parse_args()

    # 解析模式：默认全部；指定任意子开关时只执行对应部分
    run_reset = args.reset
    has_specific_mode = args.users or args.profile or args.cases or args.events or args.repair_derived_ids or run_reset
    if run_reset:
        # --reset 为独立模式：重置后执行完整种子注入
        run_users = True
        run_profile = True
        run_events = True
        run_cases = True
        run_repair = False
    else:
        run_users = args.users or args.profile or args.events or not has_specific_mode
        run_profile = args.profile or args.events or not has_specific_mode
        run_events = args.profile or args.events or not has_specific_mode
        run_cases = args.cases or not has_specific_mode
        run_repair = args.repair_derived_ids

    settings = get_settings()
    database_url = str(settings.DATABASE_URL)

    engine = create_async_engine(database_url, echo=False)
    session_factory = async_sessionmaker(
        engine,
        class_=AsyncSession,
        expire_on_commit=False,
    )

    async with session_factory() as session:
        if run_reset:
            reset_ok = await _reset_database(session, yes=args.yes)
            if not reset_ok:
                return 0

        if args.clean and run_cases and not run_reset:
            print("[INFO] 清空种子案例数据...")
            await _clear_seed_data(session)

        judge_user_id: UUID | None = None
        if run_users:
            print("[INFO] 确保评委用户存在...")
            judge_user_id = await _ensure_judge_user(session)
        elif run_profile or run_cases:
            # 后续步骤依赖评委用户作为默认作者/档案所有者
            config = _get_judge_config()
            result = await session.execute(select(User.id).where(User.username == config["username"]))
            judge_user_id = result.scalar_one_or_none()
            if judge_user_id is None:
                print(
                    "[ERROR] 未找到评委用户，请先运行 --users 或完整注入",
                    file=sys.stderr,
                )
                return 1

        profile_id: UUID | None = None
        if run_profile:
            print("[INFO] 确保默认患者档案存在...")
            profile_id = await _ensure_profile(session, judge_user_id)

        if run_events:
            if profile_id is None:
                result = await session.execute(
                    select(Profile.profile_id).where(
                        Profile.caregiver_id == judge_user_id,
                        Profile.nickname == PROFILE_NICKNAME,
                    )
                )
                profile_id = result.scalar_one_or_none()
                if profile_id is None:
                    print(
                        "[ERROR] 未找到默认患者档案，请先运行 --profile 或完整注入",
                        file=sys.stderr,
                    )
                    return 1
            print("[INFO] 确保事件记录存在...")
            await _ensure_events(session, profile_id, judge_user_id)

        if run_cases:
            print(f"[INFO] 导入 {len(_SEED_CASES)} 个案例...")
            created_cards = await _import_cases(session, judge_user_id)
            if created_cards:
                print(f"[INFO] 为 {len(created_cards)} 张新卡片生成向量索引...")
                await _index_cards(session, created_cards)
            else:
                # 如果本次没有新卡片，尝试为所有 pending/failed 的种子卡片重新索引
                result = await session.execute(
                    select(CaseCard)
                    .join(CaseNarrative)
                    .where(
                        CaseNarrative.is_seed == True,  # noqa: E712
                        CaseCard.index_status != "indexed",
                    )
                )
                pending_cards = list(result.scalars().all())
                if pending_cards:
                    print(f"[INFO] 为 {len(pending_cards)} 张待索引卡片生成向量...")
                    await _index_cards(session, pending_cards)
                else:
                    print("[SKIP] 所有种子卡片已完成索引")

        if run_repair:
            print("[INFO] 修正 derived_card_ids 不一致的数据...")
            await _repair_derived_card_ids(session)

        # 咨询历史：默认注入一条，与案例库种子数据配套展示
        if run_users or run_profile or run_cases:
            print("[INFO] 确保咨询历史记录存在...")
            # 此时 judge_user_id 必然已存在
            if judge_user_id is None:
                result = await session.execute(select(User.id).where(User.username == JUDGE_USERNAME))
                judge_user_id = result.scalar_one_or_none()
            if judge_user_id is not None:
                await _ensure_consultation(session, judge_user_id)
            else:
                print(
                    "[WARN] 未找到评委用户，跳过咨询历史注入",
                    file=sys.stderr,
                )

    await engine.dispose()
    print("\n[OK] 种子注入完成")
    return 0


if __name__ == "__main__":
    sys.exit(asyncio.run(main()))
